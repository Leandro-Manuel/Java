package Polimorfismo;

public class UmNome {
	
	
	public float adicao(float a,float b) {
		return a + b;	
	}
	
	public float adicao(float a, float b, float c) {
		return a + b + c;
	}
	
	//
	
	public float multiplicacao(float a, float b) {
		return a + b;
	}
	
	public float multiplicacao(float a, float b, float c) {
		return a + b + c;
	}
	
	//
	
	public float divisao(float a, float b) {
		return a / b;
	}
	
	public float divisao(float a, float b, float c) {
		return (a / b) / c;
	}
	
	//
	
	public float subtracao(float a, float b) {
		return a - b;
	}
	
	public float subtracao(float a, float b, float c) {
		return a - b - c;
	}
	
	
	
}
