package fundamentos;

public class variaveis {
	public static void main(String[] args) {
		int x = 10;
		System.out.println(x++); //primeiro printa x e depois adiciona mais 1 (x++)
		System.out.println(++x); //primeiro soma e depois printa (++x)
		System.out.println(x--); 
		System.out.println(--x);
		
		
	}
}
